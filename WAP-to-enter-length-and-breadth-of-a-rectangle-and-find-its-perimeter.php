<?php
// PHP program to find the perimeter of a rectangle
$l = 5;
$w = 7;
$p = NULL;

// Calculate perimeter of rectangle
$p = 2 * ($l + $w);
echo "\nThe Perimeter of rectangle = " . $p . " units"
?>

<!-- Formula:
P = 2(l + w)

where:
P = perimeter
l = length of the rectangle
w = width of the rectangle -->